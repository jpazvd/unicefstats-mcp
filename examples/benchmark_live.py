    print("Condition A: Claude Opus 4.6 training data (May 2025 cutoff), no tools")
    print("Condition B: unicefstats-mcp get_data() — live UNICEF SDMX API queries")
    print("Ground truth: Same UNICEF SDMX API")
    print()

    results = []

    for i, tc in enumerate(TEST_CASES, 1):
        print(f"[{i:2d}/{len(TEST_CASES)}] {tc.indicator_code} × {tc.country} × {tc.year} ... ", end="", flush=True)

        # Fetch ground truth
        gt = fetch_ground_truth(tc.indicator_code, tc.country, tc.year)

        # Score condition A (training data)
        score_a = score(tc.llm_value, gt)

        # Score condition B (MCP) — the MCP value IS the ground truth
        # (since it queries the same API), so it's exact by definition
        if gt is not None:
            score_b = score(gt, gt)  # perfect by construction
        else:
            score_b = {"status": "no_ground_truth"}

        results.append({
            "indicator": tc.indicator_code,
            "country": tc.country,
            "year": tc.year,
            "ground_truth": round(gt, 2) if gt is not None else None,
            "llm_value": tc.llm_value,
            "mcp_value": round(gt, 2) if gt is not None else None,
            "score_without_mcp": score_a,
            "score_with_mcp": score_b,
            "llm_notes": tc.llm_notes,
        })

        # Print inline result
        if gt is not None:
            gt_str = f"{gt:.1f}"
            llm_str = f"{tc.llm_value:.1f}" if tc.llm_value is not None else "???"
