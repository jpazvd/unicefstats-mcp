def fetch_ground_truth(indicator: str, country: str, year: int) -> float | None:
    """Fetch the real value from the UNICEF SDMX API via unicefstats-mcp."""
    result = get_data(
        indicator=indicator,
        countries=[country],
        start_year=year,
        end_year=year,
        format="compact",
        limit=5,
    )
    if "error" in result:
        return None
    if not result.get("data"):
        return None
    return result["data"][0].get("value")


def score(predicted: float | None, actual: float | None) -> dict:
    """Score a prediction against ground truth."""
    if actual is None:
        return {"status": "no_ground_truth"}
    if predicted is None:
        return {"status": "no_prediction", "error": actual}

    abs_err = abs(predicted - actual)
    pct_err = (abs_err / actual) * 100 if actual != 0 else float("inf")
    signed_err = predicted - actual

    return {
        "status": "scored",
        "absolute_error": round(abs_err, 2),
        "percentage_error": round(pct_err, 2),
        "signed_error": round(signed_err, 2),
        "exact_match": pct_err < 0.1,
        "within_1pct": pct_err < 1.0,
        "within_5pct": pct_err < 5.0,
        "within_10pct": pct_err < 10.0,
        "direction": "over" if signed_err > 0 else "under" if signed_err < 0 else "exact",
    }


